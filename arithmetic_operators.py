# Declaring variables
x = 5
y = 10

# Addition
result_add = x + y
print(result_add)

# Subtractions
result_subtract = x - y
print(result_subtract)

# Multiplication
result_multiply = x * y
print(result_multiply)

# Division
result_divide = x / y
print(result_divide)

# Modulus
result_remainder = x % y
print(result_remainder)

# Exponential
result_exponential = x ** y
print(result_exponential)

# Floor Division
result_floorDivide = x // y
print(result_floorDivide)

# Compound Assignment Operators (+=, -=, *=, /=, %=, **=, //=)
x += 3 # Equivalent to x = x + y
print(x)

