# Declaring variables
x = 5
y = 10

# Equal to (==)
result_qualTo = x == y
print(result_qualTo)

# Not Equal
result_notEqual = x != y
print(result_notEqual)

# Greater than
result_greaterThan = x > y
print(result_greaterThan)

# Less than
result_lessThan = x < y
print(result_lessThan)

# Greater than or equal to
result_greaterThanOrEqualTo = x >= y
print(result_greaterThanOrEqualTo)

# Less than or equal to
result_lessThanOrEqualTo = x <= y
print(result_lessThanOrEqualTo)